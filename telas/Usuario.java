
package telas;

public class Usuario {
    
    private String ra, senha, nome, dataNasc;
    
    public Usuario(String ra, String senha, String nome, String dataNasc){
        this.ra = ra;
        this.senha = senha;
        this.nome = nome;
        this.dataNasc = dataNasc;
    }
    
    public String getRa() {
        return ra;
    }

    public void setRa(String ra) {
        this.ra = ra;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDataNasc() {
        return dataNasc;
    }

    public void setDataNasc(String dataNasc) {
        this.dataNasc = dataNasc;
    }
    
    
    
}
