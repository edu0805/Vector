    private void carregarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setNumRows(0);
            Connection con = ConnectionFactory.getConnection();
            ResultSet rs = null;
            Object dados[] = new Object[3]; // seis posições, seis colunas
            PreparedStatement stm = null;
            try {
                stm = con.prepareStatement("SELECT * FROM vector.usuario");
                rs = stm.executeQuery();
                while (rs.next()) {
                dados[0] = rs.getString("ra");
                dados[1] = rs.getString("nome");
                dados[2] = rs.getString("datnasc");
                modelo.addRow(dados);
                
                System.out.println(rs.getString("ra"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManterUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        ConnectionFactory.closeConnection(con, stm, rs);
        }
