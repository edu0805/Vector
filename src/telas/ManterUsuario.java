    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Connection con = ConnectionFactory.getConnection();
        PreparedStatement stm = null;

        try {
            stm = con.prepareStatement("UPDATE Vector.usuario SET ra=?,senha=?,nome=?,datnasc=? WHERE ra=?");
            stm.setString(1, jTextField1.getText());
            stm.setString(2, jTextField4.getText());
            stm.setString(3, jTextField2.getText());
            stm.setString(4, jTextField3.getText());
                
            stm.setString(5, jTextField1.getText());
            
            stm.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Dados ALTERADOS com sucesso", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(ManterUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        ConnectionFactory.closeConnection(con, stm);
        limparCampos();
        carregarTabela();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       if (jTable1.getSelectedRow() != -1) {
            Connection con = ConnectionFactory.getConnection();
            PreparedStatement stm = null;
            try {
                stm = con.prepareStatement("DELETE FROM Vector.usuario WHERE ra=?");
                stm.setString(1, jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                stm.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Dados REMOVIDOS com sucesso", "Aviso", JOptionPane.INFORMATION_MESSAGE);

            } catch (SQLException ex) {
                Logger.getLogger(ManterUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
            ConnectionFactory.closeConnection(con, stm);
            limparCampos();
            carregarTabela();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

